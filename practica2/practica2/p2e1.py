"""hacer un diagrama para calcular y mostrar area de un triangulo"""

print("Para calcular area de un triangula no faltan base y altura")

base=int(input("introduce por favor base: "))

altura=int(input("introduce por favor altura: "))

area=(base*altura)/2

print("la area del triangulo con base' {0}' y altura '{1}' es: '{2}' ".format(base,altura,area))
